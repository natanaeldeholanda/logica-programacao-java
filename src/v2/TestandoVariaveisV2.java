package v2;

public class TestandoVariaveisV2 {
    public static void main(String[] args) {
        char caractere = 'a';
        char masculino = 'M';
        char feminino = 'F';
        System.out.println("Caractere: " + caractere);
        System.out.println("Masculino: " + masculino);
        System.out.println("Feminino: " + feminino);

        System.out.println("---***---");
        String palavra = "Olá, Mundo!";
        String fraseGrande = "Oi, aqui é um exemplo de uma String, que pode ter várias palavras e caracteres especiais, como: @#$%&*()_+|~`";
        System.out.println("Palavra: " + palavra);
        System.out.println("Frase Grande: " + fraseGrande);

        System.out.println("---***---");
        boolean verdadeiro = true;
        boolean falso = false;
        System.out.println("Verdadeiro: " + verdadeiro);
        System.out.println("Falso: " + falso);

    }
}
